#!/usr/bin/env bash
set -euo pipefail

# Compare the bsg100 hardware-tested panel evidence with this repository's
# live Gemian capture.  The external tree is input-only; no vendor material is
# copied into this repository.

script_dir=$(cd -- "$(dirname -- "$0")" && pwd)
repo_root=${2:-"$(cd -- "$script_dir/../../.." && pwd)"}
bsg100_tree=${1:-/private/tmp/bsg100-gemini-linux-ref}
result_dir="$repo_root/experiments/2026-07-13-bsg100-gemini-linux-comparison/results"
mkdir -p "$result_dir"

require_file() {
	if [[ ! -f "$1" ]]; then
		echo "FAIL missing file: $1" >&2
		exit 1
	fi
}

require_marker() {
	local file=$1
	local marker=$2
	if ! rg -F -q -- "$marker" "$file"; then
		echo "FAIL missing marker in $file: $marker" >&2
		exit 1
	fi
}

if [[ ! -d "$bsg100_tree/.git" ]]; then
	echo "FAIL bsg100 tree is not a Git checkout: $bsg100_tree" >&2
	exit 1
fi

harvest="$bsg100_tree/logs/2026-07-11-stock-vendor-harvest/dispdump.log"
dmesg="$bsg100_tree/logs/2026-07-11-stock-vendor-harvest/dmesg-full.log"
session="$bsg100_tree/logs/2026-07-12-131-panel-live-dcs-session.log"
boot="$bsg100_tree/logs/2026-07-12-133-panel-video-mode-fixed-init-boot.log"
reference_notes="$bsg100_tree/boot.md"
current="$repo_root/experiments/2026-07-11-gemini-panel-recovery/results/runtime-summary.txt"

for file in "$harvest" "$dmesg" "$session" "$boot" "$reference_notes" "$current"; do
	require_file "$file"
done

require_marker "$harvest" 'LCM Driver=[aeon_ssd2092_fhd_dsi_solomon]'
require_marker "$harvest" 'xres=1080, yres=2160'
require_marker "$harvest" 'Current display driver status=video mode + CMDQ Enabled'
require_marker "$dmesg" 'VERSION : 0x41554f00'
require_marker "$dmesg" 'VERSION : 0x35393900'
require_marker "$dmesg" 'VERSION : 0x53534400'
require_marker "$dmesg" 'VERSION : 0x32303932'
require_marker "$dmesg" 'VERSION : 0x00000016'
require_marker "$session" 'dcs read 0x0f ret=1 val=0x80'
require_marker "$session" 'dcs read 0x45 ret=1 val=0x00'
require_marker "$session" 'dcs read 0x0a ret=1 val=0x1c'
require_marker "$boot" 'Solomon SSD2092 FHD DSI panel registered'
require_marker "$boot" 'post-init: DCS read 0x0a ret=1 val=0x1c'
require_marker "$reference_notes" 'The real panel is a Solomon SSD2092, FHD'
require_marker "$reference_notes" 'VSA/VBP/VFP=1/43/76, HSA/HBP/HFP=4/20/26'
require_marker "$current" 'display.driver=aeon_nt36672_fhd_dsi_vdo_x600_xinli'
require_marker "$current" 'display.vendor_bootloader_expected_id=0x8070'
require_marker "$current" 'display.option.use_device_tree=0'
require_marker "$current" 'display.resolution=1080x2160'
require_marker "$current" 'display.vendor_lcm_candidates=aeon_nt36672_fhd_dsi_vdo_x600_xinli;aeon_ssd2092_fhd_dsi_solomon'
require_marker "$current" 'display.vendor_dmesg_mixed_label=NT36672_lcm_suspend;SSD2092_lcm_suspend;origin_unresolved'
require_marker "$current" 'touch.solomon_candidate=i2c4-0x53;solomon_touch;unbound'
require_marker "$current" 'touch.active_candidate=i2c4-0x62;cap_touch;NVT-ts;bound'

reference_commit=$(git -C "$bsg100_tree" rev-parse HEAD)
current_hash=$(shasum -a 256 "$current" | awk '{print $1}')

output="$result_dir/bsg100-panel-crosscheck-20260714.txt"
{
	printf '%s\n' '# Generated by audit-bsg100-panel-crosscheck.sh'
	printf 'audit_script_sha256=%s\n' "$(shasum -a 256 "$script_dir/$(basename "$0")" | awk '{print $1}')"
	printf 'reference_repo=bsg100/gemini-linux\n'
	printf 'reference_commit=%s\n' "$reference_commit"
	printf 'reference_harvest_sha256=%s\n' "$(shasum -a 256 "$harvest" | awk '{print $1}')"
	printf 'reference_dmesg_sha256=%s\n' "$(shasum -a 256 "$dmesg" | awk '{print $1}')"
	printf 'reference_dcs_session_sha256=%s\n' "$(shasum -a 256 "$session" | awk '{print $1}')"
	printf 'reference_boot_sha256=%s\n' "$(shasum -a 256 "$boot" | awk '{print $1}')"
	printf 'reference_notes_sha256=%s\n' "$(shasum -a 256 "$reference_notes" | awk '{print $1}')"
	printf 'current_runtime_summary_sha256=%s\n' "$current_hash"
	printf 'reference_runtime_lcm=aeon_ssd2092_fhd_dsi_solomon\n'
	printf 'reference_hardware_probe=SEEPROM_words_AUO_599_SSD_2092_version_0x16\n'
	printf 'reference_direct_dcs=0x0f=0x80;0x45=0x00;0x0a=0x1c\n'
	printf 'reference_mode=1080x2160;video;four_lane;RGB888\n'
	printf 'reference_bias_path=I2C_0x3e;vendor_lp3101_name\n'
	printf 'current_runtime_lcm=aeon_nt36672_fhd_dsi_vdo_x600_xinli\n'
	printf 'current_runtime_mode=1080x2160;DSI_video;driver_name_only\n'
	printf 'current_vendor_lcm_candidates=aeon_nt36672_fhd_dsi_vdo_x600_xinli;aeon_ssd2092_fhd_dsi_solomon\n'
	printf 'current_mixed_log_label=NT36672_lcm_suspend;SSD2092_lcm_suspend;origin_unresolved\n'
	printf 'current_touch_topology=solomon_touch@0x53_unbound;cap_touch@0x62_NVT-ts_bound\n'
	printf 'current_expected_id=0x8070;vendor_compare_id_unconditional\n'
	printf 'current_dt_selection=DISP_OPT_USE_DEVICE_TREE_0\n'
	printf 'current_mainline_consumer=disabled\n'
	printf 'identity_assessment=conflict;current_tree_contains_legacy_solomon_touch_candidate;reference_direct_probe_names_display_ssd2092\n'
	printf 'shared_contract=1080x2160;single_DSI_video_geometry_is_correlated\n'
	printf 'non_shared_contract=initialization_table;controller_identity;readback_values\n'
	printf 'decision=do_not_call_NT36672_or_SSD2092_mainline_descriptor_hardware_proven_without_direct_probe_on_named_device\n'
	printf 'next_probe=controlled_DSI_readback_or_panel_SEEPROM_capture_on_named_device_before_enabling_consumer\n'
	printf 'runtime_mainline=not_attempted\n'
} > "$output"

chmod 0600 "$output"
cat "$output"
